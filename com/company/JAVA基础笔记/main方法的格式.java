package com.company.JAVA基础笔记;


public class main方法的格式 {
    public static void main(String[] args){
        /*
        * public:公共的，访问权限是最大的，由于main方法是被jvm调用，所以权限要足够大
        * static:静态的，不需要创建对象，通过雷鸣就可以，方便jvm的调用
        * void:无返回值，因为main方法是被jvm调用，返回内容给jvm无意义
        * main:常见方法入口
        * String[] args:早期为了接收键盘录入的数据
        * */
    }
}
